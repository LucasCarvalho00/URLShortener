import SwiftUI

enum DSSpacing {

    /// 4 pt – espaçamento mínimo, detalhes bem próximos
    static let xxs: CGFloat = 4

    /// 8 pt – espaçamento pequeno
    static let xs:  CGFloat = 8

    /// 12 pt – espaçamento curto entre elementos relacionados
    static let sm:  CGFloat = 12

    /// 16 pt – espaçamento padrão entre blocos
    static let md:  CGFloat = 16

    /// 24 pt – espaçamento grande, separação de seções
    static let lg:  CGFloat = 24

    /// 40 pt – espaçamento extra grande, respiro de tela
    static let xl:  CGFloat = 40
}

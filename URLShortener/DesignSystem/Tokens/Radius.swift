import SwiftUI

enum DSRadius {

    /// 6 pt – arredondamento sutil, usado em elementos menores
    static let sm:   CGFloat = 6

    /// 12 pt – arredondamento padrão para cards e campos
    static let md:   CGFloat = 12

    /// 20 pt – arredondamento mais evidente, botões maiores / containers
    static let lg:   CGFloat = 20
}
